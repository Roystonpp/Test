package MainPack;

public class Compass //Attributes
{
    private String north, south, east, west;


    public Compass() //Constructor
    {

        this.north = north;
        this.east = east;
        this.south = south;
        this.west = west;
    }

    public String getNorth()  //Methods
    {
        return this.north;
    }
    public String getEast()
    {
        return this.east;
    }
    public String getSouth()
    {
        return this.south;
    }
    public String getWest()
    {
        return this.west;
    }
}
